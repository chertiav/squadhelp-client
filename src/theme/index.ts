export const ColorModeContext = createContext<{ toggleColorMode: () => void }>({
	toggleColorMode: () => {},
});

export const useMode = () => {
	const localTheme: string = JSON.parse(localStorage.getItem('mode') as string);
	const [mode, setMode] = useState(localTheme ? localTheme : 'light');
	const colorMode = useMemo(
		() => ({
			toggleColorMode: () => {
				setMode((prev) => (prev === 'light' ? 'dark' : 'light'));
				mode === 'dark'
					? localStorage.setItem('mode', JSON.stringify('light'))
					: localStorage.setItem('mode', JSON.stringify('dark'));
			},
		}),
		[mode],
	);

	const thema: any = useMemo(() => createTheme(themeSettings(mode)), [mode]);
	return [thema, colorMode];
};
